
/*Дан односвязный линейный список и указатель на голову списка P1. Необходимо
вставить значение M перед каждым вторым элементом списка, и вывести ссылку на последний
элемент полученного списка P2. При нечетном числе элементов исходного списка в конец
списка вставлять не надо.


Для каждой динамической структуры должен быть предусмотрен стандартный набор методов -
добавления/удаления/вывода элементов.
Во всех задачах обязательно наличие дружественного интерфейса. Ввод данных с клавиатуры.

*/
#include <windows.h>
#include <iostream>
#include "Header.h"

using std::cout;
using std::endl;

int main() {
    SetConsoleCP(1251);
    SetConsoleOutputCP(1251);

    LinkedList list;

    cout << "Введите количество элементов списка: ";
    int n;

    //FIX_ME: не было проверки корректности ввода
    // старый код
    // cin >> n;
    // новый код
    if (!safe_input(n) || n <= 0) {
        cout << "Ошибка: некорректное количество элементов." << endl;
        return 0;
    }

    for (int i = 0; i < n; i++) {
        int num;
        cout << "Введите элемент: ";

        //FIX_ME: не было проверки корректности ввода
        // старый код
        // cin >> num;
        // новый код
        if (!safe_input(num)) {
            cout << "Ошибка: введено нечисловое значение." << endl;
            return 0;
        }

        list.add_node(num);
    }

    cout << "Введите значение M: ";
    int m;

    if (!safe_input(m)) {
        cout << "Ошибка: введено нечисловое значение." << endl;
        return 0;
    }

    cout << "Исходный список:" << endl;
    list.print_list();

    list.insert_before_every_second(m);

    cout << "После вставки:" << endl;
    list.print_list();

    auto last = list.get_last_node();
    cout << "Адрес последнего элемента P2: " << last << endl;

    return 0;
}

